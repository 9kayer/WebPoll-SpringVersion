package com.ninekayer.webpoll.application.dto.genre;

public class GenreResponse {

    private int id;
    private String name;

    public GenreResponse() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
